TEST(DeterministicMathTests, SinDeterminism) {
    for (int i = -10; i <= 10; ++i) {
        double x = i * 0.314159;
        EXPECT_EQ(dm::sin(x), dm::sin(x));
    }
}

TEST(DeterministicMathTests, ExpDeterminism) {
    for (int i = -5; i <= 5; ++i) {
        double x = i * 1.5;
        EXPECT_EQ(dm::exp(x), dm::exp(x));
    }
}
