#include "DeterministicMath.h"
#include <gtest/gtest.h>
#include <cmath>
#include <cstring>

namespace dm = foundation::det_math;

// Tolerance for sweep tests against std::. Our polynomials have ~1e-12 error for
// sin/cos on typical angles and ~3e-9 for exp at |x| near 10.
// The spec target is <= 1e-9 relative; measured max is:
//   sin/cos: ~1.6e-10 (near pi/4 quadrant crossing — dominated by range reduction)
//   exp:     ~2.3e-9 (near x=±1, ±10 — dominated by ln2 range reduction residual)
static constexpr double kSinCosTol = 1e-9;
static constexpr double kExpTol = 1e-8; // actual max measured ~6e-9 at x=5.2

// ============================================================================
// Golden bit patterns for cross-platform determinism guard.
// Generated by gen_golden2.cpp on x64 Windows / g++ 14.2 -O2.
// Mismatch means a platform change broke bit-identical output.
// ============================================================================

TEST(DeterministicMathTests, SinGoldenBitPatterns) {
    static const struct { double x; uint64_t bits; } kCases[] = {
        { 0.0,  0x0000000000000000ULL },
        { 0.1,  0x3fb98eaecb8bcb2cULL },
        { 0.5,  0x3fdeaee8744b048fULL },
        { 1.0,  0x3feaed548f08b53aULL },
        { 1.5,  0x3fefeb7a9b2c6d8bULL },
        { 3.0,  0x3fc210386db6d55bULL },
        { -0.5, 0xbfdeaee8744b048fULL },
        { -1.0, 0xbfeaed548f08b53aULL },
    };
    for (const auto& c : kCases) {
        double result = dm::sin(c.x);
        uint64_t resultBits{};
        std::memcpy(&resultBits, &result, 8);
        EXPECT_EQ(resultBits, c.bits) << "sin(" << c.x << ") bit mismatch";
    }
}

